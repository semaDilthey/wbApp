import SwiftUI

public extension Color {
    
    struct Brand {
        static let bg = Color("bg")
        static let `default` = Color("default")
        static let light = Color("light")
        static let dark = Color("dark")
        static let darkMode = Color("darkMode")
    }
    
    struct Accent {
        static let error = Color("error")
        static let safe = Color("safe")
        static let success = Color("success")
        static let warning = Color("warning")
    }
    
    struct Neutural {
        static let active = Color("active")
        static let BG = Color("BG")
        static let dark = Color("dark")
        static let disabled = Color("disabled")
        static let divider = Color("divider")
        static let secondaryBG = Color("secondaryBG")
        static let secondaryText = Color("secondaryText")
        static let text = Color("text")
    }

}
